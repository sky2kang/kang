# 자주 발생하는 일반 예외 → 초보자용 안내
COMMON_HINTS = {
    "ModuleNotFoundError": "필요한 패키지가 설치되지 않았습니다. "
                           "'pip install -r requirements.txt' 를 실행하세요.",
    "ConnectionError": "키움 서버에 연결할 수 없습니다. "
                       "인터넷 연결과 로그인 상태를 확인하세요.",
    "FileNotFoundError": "필요한 파일을 찾을 수 없습니다. 설정을 확인하세요.",
    "ValueError": "입력값이 올바르지 않습니다. 설정값을 확인하세요.",
}


def explain_kiwoom_error(code):
    """키움 반환 코드를 한글 메시지로 변환"""
    meaning, solution = KIWOOM_ERROR_MAP.get(
        code, ("알 수 없는 오류", "키움 고객센터(1544-9000)에 문의하세요.")
    )
    msg = f"[오류 {code}] {meaning}"
    if solution:
        msg += f"\n  → 해결: {solution}"
    return msg


def explain_exception(exc):
    """파이썬 예외를 초보자용 한글 안내로 변환"""
    exc_type = type(exc).__name__
    hint = COMMON_HINTS.get(exc_type, "")
    msg = f"문제가 발생했습니다: {exc}"
    if hint:
        msg += f"\n  → {hint}"
    return msg
